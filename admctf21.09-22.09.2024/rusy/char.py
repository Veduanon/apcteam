# Пример результатов
results = [65, 100, 109, 67, 84, 70, 55, 51, 104, 95, 97, 78, 99, 49, 51, 78, 116, 95, 117, 115, 53, 111, 118, 95, 107, 48, 117, 78, 116, 95, 49, 115, 95, 119, 48, 78, 100, 51, 102, 117, 108, 125]

